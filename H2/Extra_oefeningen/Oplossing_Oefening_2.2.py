diameter_fietswiel = float(input("Geef de gewenste diameter in inches:"))
PI = 3.14
afgelegde_weg_omwenteling = diameter_fietswiel * PI
afgelegde_weg_omwenteling_in_meters = diameter_fietswiel * PI * 0.025

print('de diameter van het fietswiel is',(diameter_fietswiel),"inches")
print('de afgelegde weg na een omwenteling is',(afgelegde_weg_omwenteling),'inches')
print('de afgelegde weg na een omwenteling is',(afgelegde_weg_omwenteling_in_meters),'meter')
